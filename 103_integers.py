# Numerical types
# integers and float, complex numbers, big ints

# integers
# full number
print(10)
print(type(10))

# floats
# decimal numbers
print(10.0)
print(type(10.0))

# they can be used together
print(10/3)

# add
print(3 + 4)

# subtract
print(3 - 4)

# divide
print(10/2)

# multiply
print(10*2)

# something with this thing % (modulus)
# this gives us the remainder
print(4%3)
