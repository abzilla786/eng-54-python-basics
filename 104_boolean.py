# boolean and comparison operators

# boolean
# it is a data type that is either true or false

print(true)
print(false)

print(type(true))
print(type(false))

# comparison operators
# comparison operators allow us to evaluate a condition
# they output --> 'bool'
num_a = 10
num_b = 13

# asserting if something is equal
# use double = to equate something
print(10 == '10')

print(num_a == 10)

# greater than
print(num_a > 10)
print(num_b > 10)

# smaller than
print(num_a < 10)
print(num_b < 10)

# greater or equal to
print(num_a >= 10)
print(num_b >= 10)

# smaller or equal to
print(num_a <= 10)
print(num_b <= 10)

# not equal
print(10 != 13)

# is (of something)

print(0 is 10)
